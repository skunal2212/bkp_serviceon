	});

	$('#register-section-form').bootstrapValidator({
    container: function ($field, validator) {
      return $field.parent().next('.messageContainer');
    },

    feedbackIcons: {
      validating: 'glyphicon glyphicon-refresh'
    },
    fields: {
      'gname': {
        validators: {
          notEmpty: {
            message: 'Garage Name is required and cannot be empty'
          },
        }
      },

      'oname': {
        validators: {
          notEmpty: {
            message: 'Owner Name is required and cannot be empty'
          },
        }
      },

      'phone': {
        validators: {
          notEmpty: {
            message: 'Phone is required and cannot be empty'
          },
          regexp: {
            regexp: '^[7-9][0-9]{9}$',
            message: 'Please enter valid vehicle number.'
          }
        }
      },
      'email': {
        validators: {
          notEmpty: {
            message: 'Email is required and cannot be empty'
          },
          regexp: {
            regexp: '^[^@\\s]+@([^@\\s]+\\.)+[^@\\s]+$',
            message: 'Please enter valid email.'
          }
        }
      },

      'city': {
        validators: {
          notEmpty: {
            message: 'City is required and cannot be empty'
          }
        }
      },
      
    }
  }).on('success.form.bv', function (event, data) {
    event.preventDefault();

    var form = $(this);
    var url = form.attr('action');
    form = new FormData(form[0]);

    $.ajax({
      type: "POST",
      url: url,
      data: form,
      processData: false,
      contentType: false,
      success: function(data)
      {
        data = JSON.parse(data);
        if(data.status == '1'){
           alert(data.msg);
           location.reload();
        }
      }
    });

    return false;
  });
</script>